# 1. Operadores Aritméticos
# int e float
a = 10
b = 3
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b
divisao_inteira = a // b
exponenciacao = a ** b

# 2. Operadores de Comparação
# bool
x = 5
y = 10
igual = x == y
diferente = x != y
menor_que = x < y
maior_que = x > y
menor_ou_igual = x <= y
maior_ou_igual = x >= y

# 3. Operadores Lógicos
# bool
p = True
q = False
e_logico = p and q
ou_logico = p or q
nao_logico = not p

# 4. Operadores de Identidade
# bool
lista1 = [1, 2, 3]
lista2 = [1, 2, 3]
e_igual = lista1 == lista2
e_identico = lista1 is lista2
nao_e_identico = lista1 is not lista2

# 5. Operadores de Associação
# bool
frutas = ["maçã", "banana", "laranja"]
tem_maca = "maçã" in frutas
nao_tem_abacaxi = "abacaxi" not in frutas
